public class Trangle
{
  public static void main(String args[])
	{
	 int i,j;
//outer loop to handle no. of rows
	 for(i=0;i<7;i++)
	  {//inner loop to handle the no. of column
	   for(j=0;j<=i;j++)
	   {
	     //printing stars
	   System.out.print("*");
	    }
	System.out.println();//space
	  }	

	}

}